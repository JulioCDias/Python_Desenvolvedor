# Função de potencia
def power(num): return num**2


print(power(5))
print(power(9))

# Função verificar de o numero e par ou impar


def isPar(num): return num % 2 == 0


print(isPar(5))
print(isPar(6))

# Função que devide um numero por outro


def div_num(x, y): return x / y


print(div_num(10, 2))

# Função lambda que inverte uma string


def invert_string(string): return string[::-1]


print(invert_string("Python"))
print(invert_string("Julio Cesar Dias Dos Santos"))
